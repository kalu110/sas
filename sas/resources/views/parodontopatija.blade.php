          <h5>3. Pušenje</h5>
                
          <p>Brojne kliničke studije dokazale su da duvanski dim direktno toksično deluje na desni oštećujući njihovu strukturu, čak i bez prisustva bakterija. Kod pacijenata koji imaju vrhunsku oralnu higijenu dolazi do destrukcije ( propadanja ) tkiva usne duplje uključujući i tkivo parodoncijuma.<br>
          Duvanki dim sadrzi toksične supstance koje štete tkivu, slabeći njegovu površinsku strukturu tako što uništavaju veze izmedju ćelija koje su odgovorne za čvrstoću i kontinuitet tkiva. Nikotin direktno utiče na razgradnju medjućelijske supstance, uništavajući prirodnu imunološku odbranu tkiva.</p>

        

          <div class="row">
            <div class="col-sm-12 col-md-6">
               <h5>4. Odnosi izmedju zuba</h5>

          <p>Usled neadekvatnog zagrizaja pojedini zubi dolaze ranije u kontakt sa zubima suprotne vilice, dakle nejednak zagriz i preuranjeni kontakt pojedinih zuba mogu dovesti do razvoja patoloških procesa na tkivu oko zuba ali samo ukoliko postoji minimalna količina bakterija koja udružena sa ovim faktorom dovodi do znatno bržeg napredovanja Parodontopatije. Neadekvatan zagrizaj je najčesće posledica konzervativne ili protetske restauracije zuba.</p>
            </div>
            <div class="col-sm-12 col-md-6">
                <h5>5. Nedostajući zubi</h5>
               
          <p>Nedostajući zubi u zubnom nizu uzrokuju postojnje praznog prostora što dalje dovodi do naginjanja postojećih susednih zuba koji ograničavaju bezubo polje i remećenje medjuviličnih odnosa kao i odnosa u samom zubnom nizu. Naginjanjem susednih zuba u bezubo polje dolazi do nepovoljnog nagiba graničnih zuba, pri čemu deo njihove grizne povrsine dalje od bezubog polja dolazi u prevremeni kontakt sa gornjim zubima čime se stvara povećan pritisak prilikom zvakanja na potporni apparat (parodoncijum) graničnog zuba i dolazi do stvaranja Paradontalnog džepa i razvoja parodontopatije tzv paradentoze.</p>
