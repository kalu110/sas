        <h2>
         Parodontopatija
        </h2>
        <p>
          Paradentoza, naziv koji se u narodu koristi za <b>Parodontopatiju</b> predstavlja progresivno oboljenje tkiva koje povezuje koren zuba sa kosti i nalazi se izmedju korena zuba i zubne časice u koju je smesten zub u kosti. Ovo oboljenje se ne moze izlečiti u potpunosti, ali se adekvatnim lečenjem moze zaustaviti njeno napredovanje. Pojavom parodontopatije stradaju Gingiva, Alveolarna kost, Periodoncijum i Cement korena zuba.</p>

          <p><b>Gingiva </b>je tkivo koje se nalazi oko zuba, svetlo ruzičaste boje i čvrste konzistencije u stanju punog fiziološkog zdravlja. Prvi znaci oboljenja su promena boje gingive u tamno crvenu boju pa čak i modro ljubičastu i tada se radi o upali gingive, odnosno Upale desni - poznate kao oboljenje Gingivitis, koje sustinški predstavlja prvu odnosno uvodnu fazu u parodontopatiju (u narodu poznatu kao paradentozu).</p>

          <p>Početak parodontopatije je vezan isključivo za nagomilovnje zubnog plaka koga sacinjavaju ostaci naslage hrane na koje se praktično lepe bakterije formirajući kolonije. Ukoliko se zubni plak ne ukloni mehanički, pranjem zuba i korišćenjem sredstava kao sto su konac za zube, interdentalne (medjuzubne) četkice, water pick aparat za tuširanje prostora izmedju zuba, kao i rastvora za ispiranje usta koji sprečavaju akumulaciju dentalnog plaka, dolazi do upale desni-Gingivitisa, koji se može izlečiti adekvatnim tretmanom ultrazvučnog uklanjanja naslaga profesionalno kod stomatologa uz obaveznu obuku pacijenta i detaljno uputstvo o pravilnom održavanju oralne higijene.<br>

          Ova faza bolesti je jedina reverzibilna faza, što znaci da se bolest može u potpunoisti izlečiti jedino u ovoj fazi.</p>

         <h5>1. Zubni plak</h5>

          <p>Glavni uzrok  parodontopatije ili  “paradentoze” jesu  bakterije iz zubnog plaka. Početni plak čine  ostaci hrane koji se akumuliraju na površini zuba u početku u nevidljivim a kasnije belim slojevima. Plak predstavlja organsku osnovu koju nastanjuju bakterije jer imaju idealne uslove za zivot, temperaturu, vlagu, hranu iz ostataka hrane koja sačinjava plak i koja im je potrebna za ishranu, rast i razmnožavanje. Bakterije tako počinju da nastanjuju površine zuba narocito u predelu gde zub ulazi pod desni ( gingivalni sulkus), jer je taj prostor teže ošistiti uobišajenim pranjem zuba četkicom, već su potrebna dodatna sredstva za održavanje medjuzubne-interdentalne higijene (konac, interdentalne šetkice, water pick).</p>

          <p>Vremenom  sojevi bakterija postaju sve agresivniji i akumulacijom zubnog plaka i velike količine bakterija dolazi do razaranja tkiva koje vezuje zub u kosti. Uništavanjem tkiva parodoncijuma se produbljuje prostor oko zuba formirajuci Parodontalni džep. Kako proces napreduje sve je teže da se očisti bakterije koje napredujući dalje u dubinu dolaze do kosti. Kada dodju do alveolarne kosti, bakterije lučeći dalje svoje toksine dovode do Resorpcije i  Gubitka kosti. Na ovaj način, Parodontalni džep postaje sve veći dok se u potpunosti ne uništi tkivo i kost koja drži zub viličnom grebenu, što rezultira Ispadanjem zuba.</p>

          <div class="row">
            <div class="col-md-5 col-sm-12">
              <h5>2. Genetika</h5>
                <p>Kao i u drugim oblastima ljudskog tela genetika igra značajnu ulogu u razvoju parodontopatije. Kod nekih ljudi postoji genetska predispozicija za razvoj Parodontopatije a dokaz za to je da i minimalno prisustvo zubnog plaka dovodi do akumulacije agresivnih sojeva bakterija. Nekada je moguće da u veoma kratkom vremenskom roku dolazi do stvaranja dubokih parodontalnih džepova, koji se manifestuju brzim rasklaćenjem zuba a posledično i ispadanjem.</p>
            </div>
