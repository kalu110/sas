          <h5>Kako problemi sa zubima utiču na bebu?</h5>

          <p>Usled parodontopatije može doći do prevremenog porođaja ili rođenja bebe male težine. Zbog toga je važno da se na vreme obratite lekaru i preduzmete neophodne korake da sprečite pojavu parodontopatije. Pored toga, infekcija desni ili usta, takođe može biti opasna, jer se može proširiti na ostatak organizma, i tako izazvati komplikacije u trudnoći, ili čak spontani pobačaj.</p>
         </div>
        </div>
 

          <h5>Snimanje zuba u trudnoći</h5>

          <p>Često se postavlja pitanje da li je bezbedno za trudnicu da ide na rendgensko snimanje zuba? Iako su za rendgensko snimanje u stomatološke svrhe potrebne minimalne doze zračenja, ipak je tokom trudnoće poželjno izbegavati izlaganje rendgenskim zracima, posebno u prvih 12 nedelja trudnoće. Ako je snimanje nužno, treba da ga obavite na ispravnom uređaju, uz primenu svih mera zaštite i Vas i ploda.</p>
          <h5>Kako sprečiti probleme sa zubima u trudnoći?</h5>
 
          <p>Preventiva! Najbolji način da se izbegnu problemi sa zubima u trudnoći je redovno održavanje zuba pre trudnoće. Ukoliko planirate trudnoću, savetujemo vam da u Vaše planove uključite i posetu zubaru, kako biste otklonili postojeće probleme i dobili savete koji mogu sačuvati Vaše zube. U toku trudnoće održavajte higijenu zuba, pridržavajte se saveta zubara, redovno kontrolišite zube i obavezno se javite zubaru ukoliko osetite probleme desni. Vodite računa o ishrani, izbegavajte slatkiše, unosite dosta kalcijuma i vitamina D, koristite mekšu četkicu da biste smanjili iritaciju desni, i redovno menjajte četkicu, da biste izbegli skupljanje bakterija. Pravilna nega i brzo reagovanje, mogu otkloniti mnoge neželjene posledice.</p>

