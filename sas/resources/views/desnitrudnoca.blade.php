        <h2>
         Upala desni u trudnoći
        </h2>
        <p>
         U toku trudnoće može doći do promene na desnima. Neke žene primećuju da njihove desni izgledaju crvenije i da krvare u toku pranja dok drugim ženama desni otiču i krvare. Ove promene predstavljaju zapaljenje desni u trudnoći ili strucno <b>parodontopatija</b> poznatija kao <b>paradentoza</b>. Promene mogu početi već od drugog meseca trudnoće. Ovo zapaljenje ulazi u svoj najteži stadijum u osmom mesecu trudnoće a potom polako nestaje po rođenju deteta.</p>

          <p><b>Zapaljenje desni u trudnoći</b> najčešće se manifestuje u prednjem delu usta. Simptomi su isti kao i kod standardnog zapaljenja ali je uzrok u ovom slučaju drugačiji. Povećan nivo hormona može biti delom uzrok zapaljenja desni u trudnoći. Tokom trudnoće nivo progesterona u telu može biti 10x veći od normalnog. Ovo moze da ubrza razvoj i rast nekih bakterija koje uzrokuju zapaljenje desni. Takođe, vaš imuni sistem moze da radi drugačije tokom trudnoće. Ovo bi moglo da promeni način reagovanja vašeg tela na bakterije koje izazivaju zapaljenje desni.</p>

          <div class="row">
            <div class="col-md-6 col-sm-12">
          <ul>Ukoliko se budete pridržavali ovih navika značajno ćete umanjili efekte zapaljenja desni u trudnoći. Jako je vaćno da vodite svakodnevno brigu o svojoj oralnoj higijeni:
            <li>perite zube dva puta dnevno u trajanju od najmanje dva minuta</li>
            <li>svakodnevno čistite svoje zube koncem za čisćenje</li>
            <li>korišćenjem antibakterijskih sredstava za ispiranje usta takođe možete konstrolisati infekciju  desni. Neki stomatolozi preporučuju korišćenje sredstva za ispiranje usta koja ne sadrže alkohol  iako jos uvek nije najjasnije da li ona sredstva za ispiranje koja sadrže alkohol imaju loš efekat na trudnoću.</li>
          </ul>
          </div>
