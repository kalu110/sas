         <h5>Trošenje  zuba u trudnoći</h5>
        
          <p>Kod žena koje imaju ozbiljne jutarnje mučnine ,učestalo povraćanje izaziva trošenje gleđi unutrašnje strane prednjih zuba. Ukoliko često povraćate javite se svojoj zubarskoj ordinaciji za savet kako sprečiti propadanje zubne gleđi.</p></div>

         <div class="col-md-4 col-sm-12">
          <h5>Suva usta u trudnoći</h5>
          <p>Jedna od čestih pojava u trudnoći jesu i suva usta. Ovu pojavu mozete sprečiti uzimanjem velikih količina vode i korišćenjem tvrdih bonbona bez šećera ili žvakaće gume radi stimulisanja pljuvačne sekrecije i održavanja vlažnosti usta. Jako je važno da budete hidrirane.</p>
        </div>

         <div class="col-md-4 col-sm-12">
          <h5>Preterano lučenje pljuvačnih žlezda</h5>
          <p>Malo manje učestala je  pojava preteranog lučenja pljuvačke kod trudnica. Ovo se stanje javlja u ranijim stadijumu trudnoće i nestaje do kraja prvog trimestra. Moze biti povezano i sa pojavom mučnine.</p>
        </div>
