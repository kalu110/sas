          <p>Trudnicama se savetuje da idu na redovne preglede kod zubara. Zapaljenje desni koje se javlja u trudnoći može se lečiti metodom profesionalnog čišcenja. Ova metoda se moze raditi nezavisno od meseca u kome se trudnica nalazi ali se ipak preporučuje da to bude drugi trimestar. Agresivnije metode lečenja poput periodontalne operacije se moraju odložiti za period posle porođaja.</p>

          <h5>Granulom ili tumor u trudnoći (Pyogenic Granuloma or Pregnancy Tumor)</h5>

          <p>U proseku 2%-10% trudnica dozivi pojavu granuloma u trudnoći, koji se javlja na desnima. Takodje je poznat i kao <b>piogenični granulom</b> ili tumor u trudnoći. Možda je izraz malo prenaglašen jer on ustvari nije tumor i nije kancerogen <b> on čak nije ni opasan</b> iako izaziva nelagodnost.
            <br>
          Radi se o pojavi crvenih čvorića koji su smešteni u gornjem delu desni ali mogu se naći i drugim delovima usne duplje. Ove izrasline mogu lako da prokrvare i stvore otvorenu ranu koja posle određenog vremena postaje krasta. Ove granulome su često vezane za desni ili sluzokožu membrane malim uzdužnim tkivom.
            <br>
          Pravi uzrok  granuloma u trudnoći je nepoznat ali se smatra da je loša oralna higijena primarni faktor. Povrede, hormoni, virusi i malformacije krvnih sudova smatraju se kofaktorima.Zene koje imaju ove izrasline često imaju i zapaljenje desni u poodmaklom stadijumu.
            <br>
          Po rodjenju deteta Granulome u trudnoci obično nestaju. Ako israsline ometaju govor ili žvakanje moraju biti uklonjene pre rođenja deteta. Zabeleženo je da se skoro kod polovine žena koje odlaze na zahvat uklanjanja ovih izraslina, ponovo se javlja ovaj problem.</p>

