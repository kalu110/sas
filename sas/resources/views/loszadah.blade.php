        <h2>
         Neprijatan zadah iz usta
        </h2>
        <p>
         Svako od nas se u nekom trenutku susreo sa lošim zadahom koji može biti posledica suvih usta, bakterija, prehlade, lekova, gorušice ili karijesa. Prema mišljenju stručnjaka, ovo su deset najčešćih razloga.</p>

         <h5>1. Zubni kamenac</h5>
         <p>Ovo je čest uzrok neprijatnog zadaha. Ako zadah ne može da se ukloni jednostavnim metodama, njegov izvor je najčešće u lošoj higijeni usne duplje. Kada zube ne četkamo redovno i pravilno nakupljaju se nove zubne naslage (plak), neprestano se mineralizuju i stvaraju zubni kamenac. Zubne naslage su pravi „rasadnik“ štetnih bakterija. Dok su zubne naslage još meke, bakterije su prisutne samo na zubima i redovnim četkanjem mogu brzo da se odstrane. </p>

        <h5>2. Loš zadah zbog alkohola</h5>
        <p>Izlazak u grad uz konzumaciju alkohola za posledicu imaju više od mamurluka. Iako je tečnost, <b>alkohol</b> zapravo može da isuši usta, što podstiče bakterije koje izazivaju halitozu, medicinski izraz za loš zadah. Isto se dešava i sa <b>pićima sa kofeinom, začinjenom hranom i cigaretama.</b> Suvih usta se i budite zbog toga što ne proizvodite dovoljno pljuvačke dok spavate i eto objašnjenja za „jutarnji dah”.</p>

        <h5>3. Bakterije na jeziku kao uzrok lošeg zadaha</h5>
        <p>Glavni uzrok lošeg zadaha jesu zapravo bakterije koje se nalaye na našem jeziku. Potrebno je da očisstite jezik četkicom za zube ili strugalicom za jezik, koja je u ovom slučaju boji izbor. Izbegavajte krhke plastične, koje bi mogle da puknu, kao i metalne koje mogu biti oštre.</p>

        <h5>4. Dijeta sa malo ugljenih hidrata</h5>
        <p>Kada isključite ugljene hidrate i povećate količinu proteina koju jedete, vaše telo počinje da sagoreva masti za energiju. Taj proces stvara jedinjenja koja se zovu ketoni, koja izazivaju loš zadah. U ovom slučaju, bolja higijena zuba neće rešiti problem, jer to nije osnovni uzrok. U ovom slučaju najbolje je da maskirate dah žvakom bez šećera.</p>

        <h5>5. Uobičajene prehlade mogu izazvati loš zadah</h5>
        <p>Infekcije respiratornog trakta poput prehlade i bronhitisa takođe mogu izazvati loš zadah. Usled prehlade dolazi do dehidracije organizma sro je povoljno okruzenje za razvoj bakterija. A ako vam je začepljen nos, veća je verovatnoća da ćete pribegavati disanju na usta, što vam takodje može isušiti usta.</p>

        <h5>6. Loš zadah zbog Helicobacter pylori</h5>
        <p>Vrsta bakterije koja izaziva čireve, Helicobacter pylori, takođe može izazvati loš zadah, navodi se u studiji objavljenoj u Journal of Medical Microbiology. Lečenje ove bakterije može da ukloni loš zadah. Lekar će tražiti da uradite test na H. pylori i, ukoliko je imate, propisaće adekvatne antibiotike.</p>


          <div class="row">
            <div class="col-md-6 col-sm-12">
           <h5>7. Lekovi mogu da „uguše” protok pljuvačke</h5>
          <p>Više od 400 lekova koji se izdaju na recept i bez recepta, uključujući antidepresive i lekove za alergije, mogu da „uguše” protok pljuvačke. Ova tečnost pomaže u ispiranju hrane i bakterija, zadržavajući loš zadah. Promena lekova nije uvek opcija, tako da American Dental Association preporučuje da budete hidrirani i žvaćete žvaku bez šećera kako bi vam usta bila vlažna. U ovome mogu da pomognu i posebna oralna sredstva za ispiranje.</p>
          </div>
          <div class="col-md-6 col-sm-12 clanaka">
            
            <h5>8. Loš zadah zbog sušenog voća</h5>
          <p>Suvo voće sadrži mnogo šećera, a njima vole da se hrane bakterije koje izazivaju miris. Razumna količina suvog grožđa, koja staje u 1/4 šolje, ima 21 gram šećera, a ista količina suvih kajsija ima 17 grama. To je isto kao da ste pojeli 4-5 kašičica čistog šećera. Osim toga, sušeno voće je lepljivo, tako da se može da ostane zarobljeno na zubima i između njih. Posle ovakve užine, obavezno operite zube četkicom i očistite ih koncem za zube.</p>

          </div>
         </div>

          <h5>9. Kamen u krajnicima (tonzilolit) i loš zadah</h5>

          <p>Ove male bele skupine – sastavljene od stvrdnutih bakterija, čestica hrane, mrtvih ćelija i sluzi – bivaju zarobljene u grebenima krajnika i zadnjem delu jezika. Oni su uglavnom bezopasni, osim što izazivaju neprijatan miris. Često sami nestaju, ali ponekad proces može da se ubrza grgljanjem slanom vodom.</p>

 

          <h5>10. Loš zadah zbog nesvarene hrane</h5>

          <p>Ovo su dva simptoma GERB-a (gastroezofagealna refluksna bolest), uobičajenog digestivnog poremećaja. Loš zadah može biti od neke nesvarene hrane koja se vraća ili iritacija želudačanom kiselinom izaziva postnazalno kapanje. Ako stalno imate gorušicu, konsultujte se sa svojim lekarom.</p>


          <h5>Saveti</h5>
 
          <p>Bez obzira na uzrok zadaha, zdrava higijena usne duplje – dakle redovno i pravilno četkanje, čišćenje zubnim koncem, upotreba interdentalne četkice i vodice za usta – znači prvi korak do svežeg daha. U drugom koraku slede redovni pregledi kod stomatologa. Ako potvrde da je vaša usna duplja sasvim zdrava i da dobro sprovodite higijenu, uzroke treba tražiti drugde, dublje, u gastrointestinalnom sistemu.</p>

